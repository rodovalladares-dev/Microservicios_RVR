package com.example.Mascotas;

public class Producto {
    private int id;
    private String nombre;
    private int precio;
    private int cantidad;

    // Getters y setters
    public int getId() 
    { 
        return id; 
    }

    public String getNombre() 
    { 
        return nombre; 
    }

    public int getPrecio() 
    { 
        return precio; 
    }

    public int getCantidad() 
    { 
        return cantidad; 
    }

}